// Function with arguments
function calcRectangle(width, length) {
  return width * length;
}

let rectangle1 = calcRectangle(10, 7); // value: 70
let rectangle2 = calcRectangle(20, 10); // value: 200
let rectangle3 = calcRectangle(30, 40); // value: 1200

console.log(rectangle1 + rectangle2 + rectangle3);
