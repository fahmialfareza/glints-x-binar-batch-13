// It will print 0 to 5
for (let i = 0; i < 5; i++) {
  console.log(i);
}

const fruits = ['Apple', 'Banana', 'Orange', 'Mango', 'Melon'];
// It will print every single fruit
for (let i = 0; i < fruits.length; i++) {
  console.log(fruits[i]);
}

// Array map
fruits.map((fruit) => console.log(fruit));
